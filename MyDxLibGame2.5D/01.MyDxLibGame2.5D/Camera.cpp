#include <math.h>
#include "Player.h"
#include "Camera.h"

//-----------------------------------------------------------------------------
// @brief  コンストラクタ.
//-----------------------------------------------------------------------------
Camera::Camera()
{
	// fov設定
	SetupCamera_Perspective(45.0f * DX_PI_F / 180.0f);

	//奥行1.0f～1000までをカメラの描画範囲とする
	SetCameraNearFar(1.0f, 1000.0f);

	pos = VGet(0, 0, -880.0f);
	look = VGet(0, 0, 0);
	up = VGet(0, 1, 0);
}

//-----------------------------------------------------------------------------
// @brief  デストラクタ.
//-----------------------------------------------------------------------------
Camera::~Camera()
{
	// 処理なし.
}

//-----------------------------------------------------------------------------
// @brief  更新.
//-----------------------------------------------------------------------------
void Camera::Update()
{
	SetCameraPositionAndTargetAndUpVec(pos, look, up);
}